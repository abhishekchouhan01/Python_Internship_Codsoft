#=============================>----->INPUt<-----<========================================

number1 = input("Enter the number ")
num1 = int (number1)
sign = input('Enter the sign')

number2 = input("Enter the number ")
num2 = int (number2)


#=============================>----->  ADDITION <-----<========================================
if sign == '+':
    print ("Your result is " + str(num1+num2))

#=============================>----->  SUBTRACTION <-----<========================================

elif sign == '-':
    print ("Your result is " + str(num1-num2))

#=============================>----->  MULTIPLICTION <-----<========================================

elif sign == '*':
    print ("Your result is " + str(num1*num2))

#=============================>----->  DIVISION <-----<========================================

elif sign == '/':
    print ("Your result is " + str(num1/num2))

else :
    print ("Please Enter the sign + , - , * or / ")
    

